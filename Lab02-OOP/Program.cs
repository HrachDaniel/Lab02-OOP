using System;

namespace RectangleApp
{
    class Program
    {
        static void Main(string[] args)
        {
            // Встановлюємо кодування UTF8 для консолі, щоб коректно відображалися українські літери "і", "ї", "є".
            Console.OutputEncoding = System.Text.Encoding.UTF8;
            Console.WriteLine("=== Робота з прямокутниками (розділені файли) ==="); // Виводимо заголовок програми в консоль.

            Rectangle rect1 = new Rectangle(0, 0, 10, 10); // Створюємо перший прямокутник (rect1) з координатами (0,0) та розміром 10x10.
            Rectangle rect2 = new Rectangle(5, 5, 10, 10); // Створюємо другий прямокутник (rect2) з координатами (5,5) та розміром 10x10.

            Console.WriteLine($"\nПрямокутник 1: {rect1}"); // Виводимо інформацію про перший прямокутник.
            Console.WriteLine($"Прямокутник 2: {rect2}"); // Виводимо інформацію про другий прямокутник.

            // --- 1. Тест переміщення та зміни розміру ---

            Console.WriteLine("\n--> Переміщуємо Прямокутник 1 на (2, 2) і змінюємо розмір на 8x8..."); // Виводимо повідомлення про те, що зараз будемо змінювати перший прямокутник.

            rect1.Move(2, 2); // Викликаємо метод Move для rect1, зміщуючи його на 2 по X і на 2 по Y.
            rect1.Resize(8, 8); // Викликаємо метод Resize для rect1, встановлюючи нові розміри 8x8.

            Console.WriteLine($"Прямокутник 1 (оновлений): {rect1}"); // Виводимо оновлений стан першого прямокутника.

            // --- 2. Знаходження об'єднуючого прямокутника ---

            // Викликаємо статичний метод GetBoundingBox класу Rectangle, передаючи туди rect1 і rect2.
            // Результат зберігаємо у змінну boundingBox.
            Rectangle boundingBox = Rectangle.GetBoundingBox(rect1, rect2);

            Console.WriteLine($"\n--> Найменший прямокутник, що містить обидва:"); // Виводимо пояснення.
            Console.WriteLine(boundingBox); // Виводимо параметри знайденого об'єднуючого прямокутника.

            // --- 3. Знаходження перетину ---

            // Викликаємо статичний метод GetIntersection, щоб знайти спільну частину rect1 і rect2.
            // Результат зберігаємо у змінну intersection.
            Rectangle intersection = Rectangle.GetIntersection(rect1, rect2);

            Console.WriteLine($"\n--> Спільна частина (перетин):"); // Виводимо пояснення.

            if (intersection != null) // Перевіряємо, чи результат не дорівнює null (чи існує перетин).
            {
                Console.WriteLine(intersection); // Якщо перетин існує, виводимо його параметри.
            }
            else
            {
                Console.WriteLine("Прямокутники не перетинаються."); // Якщо intersection == null, виводимо повідомлення, що перетину немає.
            }

            // Додатковий тест: прямокутники без перетину ---

            Console.WriteLine("\n----------------------------------"); // Розділювач.
            Console.WriteLine("Тест для прямокутників, що не перетинаються:"); // Виводимо повідомлення про початок тесту для неперетинних прямокутників.

            Rectangle rA = new Rectangle(0, 0, 5, 5); // Створюємо новий прямокутник rA в координатах (0,0).
            Rectangle rB = new Rectangle(20, 20, 5, 5); // Створюємо новий прямокутник rB далеко від rA — в координатах (20,20).

            Console.WriteLine($"А: {rA}"); // Виводимо дані прямокутника A.
            Console.WriteLine($"B: {rB}"); // Виводимо дані прямокутника B.

            Rectangle noIntersect = Rectangle.GetIntersection(rA, rB); // Намагаємося знайти перетин між rA і rB.

            if (noIntersect == null) // Перевіряємо результат.
            {
                Console.WriteLine("Результат: Прямокутники не перетинаються."); // Оскільки вони далеко один від одного, має спрацювати ця умова.
            }
            else
            {
                Console.WriteLine(noIntersect); // якщо перетин раптом буде знайдено.
            }

            Console.ReadLine();             // Очікуємо натискання клавіші Enter, щоб вікно консолі не закрилося миттєво.
        }
    }
}